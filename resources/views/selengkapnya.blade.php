    belakang riwayat keturunanya berbeda, yaitu Ki Ageng Morang merupakan santri nasional (sebutan sekarang),
    karena berasal dari keleuarga priyayi/kerajaan, dan Hasan Mukmin berasal dari keturunan keluarga kyai yaitu dari
    kyai Nursadik yang merupakan adik dari Kyai Hasan Besari Tegalsari (keponakan dari kyai hasan besan). Agar tidak
    terjadi perselisihan dalam mengembangkan Agama islam, Kyai Hasan Besari sangat bijak sana akhirnya hasil babad
    hutan dari Ki Ageng Morang yang terletak di sebelah utara sungai Gendol dan hasil babad hutan Hasan Mukim di
    sebelah selatan yang di sebut Nglawu di gabungkan jadi 1 (satu) dengan sebutan desa jabung (Gabung).
